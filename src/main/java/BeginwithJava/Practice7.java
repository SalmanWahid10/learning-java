package BeginwithJava;

public class Practice7 {
	public static void main(String args[]) {
		int i,j,k,l;
		for(i=4;i>1;i--) {
			for(j=5;j<=5-2;j++) {
				System.out.print(" ");
			}
			for(k=1;k<=i-2;k++) {
				System.out.print(k);
			}
			for(l=1;l<=i-1;l++) {
				System.out.print(l);
			}
			System.out.println();
		}
	}

}
