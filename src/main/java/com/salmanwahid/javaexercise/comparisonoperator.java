package com.salmanwahid.javaexercise;

public class comparisonoperator {
	public static void main(String args[]) {
		int faribaAge = 7;
		int fabihaAge = 4;
		System.out.println(faribaAge > fabihaAge);
		System.out.println(faribaAge < fabihaAge);
		System.out.println(faribaAge == fabihaAge);
		System.out.println(faribaAge != fabihaAge);
	}

}
